export const AnimatedWordmark = (props: {
  className?: string;
  color?: string;
  outlineWidth?: number;
}): JSX.Element => {
  const [hasStarted, setHasStarted] = useState(false);
  const id = useId();

  useEffect(() => {
    const vivus = new Vivus(id, {
      duration: 100,
      start: 'manual',
      type: 'scenario',
    });
    vivus.reset();

    setTimeout(() => {
      vivus.play(1);
      setHasStarted(true);
    }, 500);
  }, []);

  return (
    <Wordmark
      id={id}
      className={props.className}
      css={css`
        opacity: ${hasStarted ? 1 : 0};
        transition: opacity 0.2s;
      `}
      color={props.color}
      outlineWidth={props.outlineWidth}
    />
  );
};

